package com.malaganguo.athmsssm.model;

public class PieChartModel {

    private double lowScopeTemp;
    private double betweenScopeTemp;
    private double highScopeTemp;

    public double getLowScopeTemp() {
        return lowScopeTemp;
    }

    public void setLowScopeTemp(double lowScopeTemp) {
        this.lowScopeTemp = lowScopeTemp;
    }

    public double getBetweenScopeTemp() {
        return betweenScopeTemp;
    }

    public void setBetweenScopeTemp(double betweenScopeTemp) {
        this.betweenScopeTemp = betweenScopeTemp;
    }

    public double getHighScopeTemp() {
        return highScopeTemp;
    }

    public void setHighScopeTemp(double highScopeTemp) {
        this.highScopeTemp = highScopeTemp;
    }
}
